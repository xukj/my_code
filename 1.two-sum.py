# @lc code=start
class Solution(object):
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        dict = {}
        for i, num in enumerate(nums):
            rest = target - num
            if rest in dict:
                return [i, dict[rest]]
            else:
                dict[num] = i
        
